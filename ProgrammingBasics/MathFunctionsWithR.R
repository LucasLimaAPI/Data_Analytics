#MathFunctionsWithR
# abs() computes the absolute value.
abs(2)
abs(-2)#dois e o valor absoluto pois a disstancia de zero pode passar para um vetor inteiro
v <- c(-2,-3,0,4)
abs(v)

# sum() return the sum of all the values present in the input.
sum(v)

# mean() computes the arithmetic mean.
mean(v)#Vai retornar a media do valor

# round() rounds values (additional arguments to nearest)
round(v)
round(2.33455421, digits = 2)#Vai arredondar valores com digitos que inserirmos 
